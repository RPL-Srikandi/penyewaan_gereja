            <p>GKJ Manahan didirikan pada tanggal 8 Februari 1929 dan berlokasi di Tumenggungan, Surakarta. Sebelumnya, GKJ Manahan merupakan bagian dari GKJ Margoyudan Surakarta. Pada tanggal 25 Desember 1931, GKJ Manahan memiliki gedung baru di Manahan yang diresmikan dalam Kebangkitan Natal oleh Ds. Van Eyk, sebagai Konsultan GKJ Manahan.</p>
            <p>Pendeta pertama yang melayani GKJ Manahan adalah Bapak Ds. Atmowidjono yang bertugas sejak tahun 1936. Jumlah warga jemaat pada awalnya hanya sekitar 50 orang, namun berkembang menjadi 239 orang pada tahun 1942, saat tentara Jepang menduduki Indonesia. Wilayah pelayanan GKJ Manahan meliputi kota Sala bagian barat, Kartasura, Pengging, Blulukan, Palur, dan Karanggede.</p>
            <p>Dengan pertumbuhan jemaat yang pesat, muncul kebutuhan untuk mengembangkan sarana fisik, seperti perluasan gedung gereja agar dapat menampung lebih banyak peserta kebaktian.</p>
            <p>Pada tanggal 13 April 1986, dimulai pembangunan Gedung Serba Guna GKJ Manahan dengan perletakan batu pertama. Gedung ini memiliki dua lantai, dengan lantai pertama digunakan untuk pertemuan, rapat, yang dapat menampung 250 orang, dan juga digunakan untuk kebaktian saat gedung gereja sedang direvitalisasi. Lantai kedua digunakan sebagai kantor gereja, kantor klasis Kartasura, dan ruang perpustakaan. Pembangunan Gedung Serba Guna selesai pada bulan Desember 1987 berkat anugerah Tuhan.</p>
